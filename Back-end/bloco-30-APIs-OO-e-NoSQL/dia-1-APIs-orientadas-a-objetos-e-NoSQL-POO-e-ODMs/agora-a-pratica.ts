// O que são os ODMs?
'ODMs (Object Document Mapping) ou na tradução literal ODMs (Mapeamento de Documento de Objeto), servem para lidar com dados estruturados em bancos de dados não relacionais (como o MongoDB, DynamoDB, entre outros).'

// Qual a diferença entre ODM e ORM?
'ORM lidam com dados estruturados em bancos de dados relacionais e ODM lidam com dados estruturados em banco de dados não relacionais';

// De acordo com a documentação do Mongoose, uma Interface, um Schema e uma Model servem para representar o quê? Dê exemplos
`o schema pode ser visto como um molde de uma collection, e será responsável por descrever toda a estrutura dos dados,
a interface é um contrato para a classe, model, schema, metodo,
a model irá prover todas as funções necessárias para acessar e manipular os dados (Documents) da nossa Collection`;

// Quais são os 4 pilares da programação orientada a objetos? Discorra sobre a definição de cada um deles e dê exemplos
`Herança: Permite que classes compartilhem atributos e métodos,
Abstração: Significa esconder os detalhes da implementação dentro de algo, abstrair o codigo é a forma que usamos para esconder os detalhes do funcionamento da nossa aplicação,
Polimorfismo: É o princípio pelo qual duas ou mais classes derivadas de uma mesma superclasse podem invocar métodos que têm a mesma identificação (assinatura) mas comportamentos distintos,
Encapsulamento: Encapsular os dados de uma aplicação significa evitar que estes sofram acessos indevidos,`;